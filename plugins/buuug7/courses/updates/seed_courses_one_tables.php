            <p style="text-indent: 2em">2005 年初国务院 2号文《国务院办公厅关于加快电子商务发展的若干意见》的正式出台和《中华人民共和国电子签名法》的实施，表明了中国政府对发展电子商务的决心和信心，也是我国电子商务发展的一个重要里程碑。作为一项庞大的社会化系统工程，电子商务的发展离不开标准化的支撑。正如国务院 2 号文中提出的：必须建立并完善电子商务国家标准规范体系，提高标准化意识，充分调动各方面积极性，抓紧完善电子商务的国家标准体系；鼓励以企业为主体，联合高校和科研机构研究制定电子商务关键技术标准和规范，参与国际标准的制订和修正，积极推进电子商务标准化进程。</p>
            <p style="text-indent: 2em">本文在此对我国电子商务标准化中存在的问题及我国电子商务标准未来的发展提出了一些建议，并提出了一系列急需制定的标准项目，旨在为我国电子商务标准的规划和计划提供参考，系统地知道和推进我国电子商务标准化工作。</p>
            <h3>我国电子商务标准体系</h3>
            <p style="text-indent: 2em">我国电子商务的编制本着在充分吸收和借鉴国际标准化组织在电子商务标准体系建设方面的成果和经验的前提下，紧密结合我国国家、行业和领域电子商务发展对标准的需求的原则，全面的、系统的进行我国电子商务标准体系建设工作。</p>
            <p style="text-indent: 2em">电子商务的核心是商务，为了更好地实现电子化的商务活动，必须建设现代物流、信用服务、在线支付、安全认证等一系列支撑体系。电子商务标准不是要解决某个政府或企业内部的业务活动规范化和信息化，而是要重点解决不同行业和不同领域的企业、政府和消费者之间在参与电子商务时的技术互操作和商务互操作问题，从而能够实现既定的商业活动和目标。从这个角度看，首先可以将电子商务标准分为两大类：即面向商务活动的业务标准和面向支撑体系的支撑体系标准。其次，为了保障电子商务的良性发展，还必须制定监督管理类标准，具体包括电子商务服务质量、发展评价和标准符合性测试等方面的内容。此外，还有一些基础技术类标准（比如 SOA、Web Services 等软件技术架构、描述技术、自动识别与标识技术以及网络协议等标准）对于电子商务的实现也是不可缺少的。 综上所述，电子商务标准可分为基础技术标准、业务标准、支撑体系标准和监督管理标准四大类别。</p>
            <h3>我国电子商务标准化存在的问题</h3>
            <ol>
                <li>标准体系不完善。随着信息技术的发展和普及，我国电子商务的得到快速发展，标准化工作也得到良好的发展，制订了一系列“基础技术标准”如：GB/T 19256-2003（《基于XML的电子商务》总共七部分）、GB/T 20538-2010（《基于XML的电子商务业务数据和过程》总共七部分）等总体通用标准；GB/T 18811-2002《电子商务基础术语》、GB/T 2312-1980《信息交换用汉字编码字符集 基本集》等面向B2B的开放式采购协议；GB/T 14805-1999《用于行政、商业和运输电子数据交换的应用级语法规则》系列标准、GB/T 18793-2002《信息技术 可扩展置标语言（XML）1.0》等描述技术标准。但是由于标准制定与市场需求之间的协调性和配套性较差，目前标准体系还存在内容不完整、标准空白等诸多问题。如电子单证格式标准完整性不足、服务质量、业务流程、安全认证、在线支付信用服务等支撑体系标准和监督管理标准缺失或不足。</li>
                <li>系统协同性较差。由于电子商务跨越的领域较多，集成的技术范围广泛，因此其标准化工作涉及的部门也较多。国家有关部门根据需求各自制定自己的标准，导致业务交叉或者重叠局面出现。在资源共享、协同分工方面，缺乏稳定、长效的沟通协调机制；在统筹规划、高效管理方面，电子商务标准化缺乏统一的维护管理机制。</li>
                <li>国际化程度较低。我国电子商务标准化发展历程较短，在标准体系、方法和关键技术等方面的基础理论研究还比较薄弱，与发达国家相比，仍处在起步阶段。虽然，近几年我国电子商务标准采用国际标准和发达国家标准的比率有所提升，但是，对国外先进标准的跟踪研究和国内外标准的关联度研究仍比较缺乏，完全与国际接轨尚需时日。</li>
                <li>市场适应性差，宣贯力度不够。我国早期电子商务发展以政府为主导，其标准大都由政府制定，企业处于接受地位，不是标准制定的主体，我国标准制定和市场需求的协调性较低，这就容易使得我国企业管理人员和执法人员标准意识薄弱，对电子商务标准了解较少，造成我国大约一半的电子商务标准还处在几乎未被使用的局面。</li>
            </ol>
