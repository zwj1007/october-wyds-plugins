            <p style="text-indent: 2em">为了配合平台的积分兑换活动，系统提供参与兑换活动商品的设置和积分换算。比如平台管理员可以将某一商品设置为积分兑换的商品，同时设置兑换所需的分值，以及用于兑换的商品数量。用户可以浏览兑换商品，查看每件商品需要的积分值和剩余的商品数，如果同意兑换，系统判断用户是否有足够的积分，有的话将积分从用户的帐户扣除，并将兑换信息反馈给平台管理人员，平台管理员将兑换的商品寄出，完成商品兑换操作。积分兑换商品的管理和平台上其它商品相同，包括展示、评论16页等。</p>
            <h4>商品展示</h4>
            <p style="text-indent: 2em">在平台上的所有商品可以按照不同的特性进行分类，方便用户的挑选。每件商品信息的展示采用滤镜的方式，可以随着鼠标的移动查看图片的细节。</p>
            <p style="text-indent: 2em">用户可以查看商品的详细信息，包括商品介绍，规格，相关评论等，系统也会跟据页面的内容给用户提供些推荐商品。用户可以将该商品添加到收藏夹或者放入购物车中。</p>
            <h4>在线推广</h4>
            <p style="text-indent: 2em">平台支持商品的在线推广，每个会员用户都可以通过管理后台获得到相应的商品推广代码，他们可以把这个代码投放到自己博客、空间、网站、网上通讯工具的个人资料里面，提供多种接触电子商务平台的渠道，以达到宣传的目的。平台对这段代码进行跟踪，统计点击率，可以根据优惠规则转化为积分等。对于通过在网上点击这段代码后进入到网站，并成功购买商品的用户，推广帐号将会获得相应的奖励，比如佣金返回等。这是一种双赢的模式，不仅推广了市场，宣传了平台，同时也给更多的会员多了一份收入。</p>
            <h4>定制信息通报</h4>
            <p>平台提供手机短信和群发邮件两种方式进行定制信息的通报。</p>
            <h4>广告发布</h4>
            <p style="text-indent: 2em">台在界面设计中预留广告位置，支持多种广告形式，包括传统的BANNER、LOGO、友情链接、浮动窗口、弹出窗口等。这种方式的广告，广告投放方直接和平台（广告发布方）结算，由后第20页台管理人员按照预留的位置大小将广告图片、文字或FLASH动画添加到相应的位置，设置显示的有效期，对于浮动窗口广告还可以设置显示延时，比如显示5秒后自动关闭。</p>
            
            <h3>信息咨询平台</h3>
            <h4>在线搜索</h4>
            <p style="text-indent: 2em">电子商务平台支持对多种语言的搜索，包括页面资讯的搜索和数据库中信息的搜索。用户可以对多种关键字（主题分类、关键字、尺寸等）进行搜索快速地寻找到自己想要的商品，查看相关的资讯等，并可以根据信息的发布时间，相关程度或产品价格等用户关心的内容进行排第21页序，满足用户对信息的搜索。</p>
            <h4>在线导购</h4>
            <p style="text-indent: 2em">在线导购系统，能够通过对访问者各方面背景、互联网冲浪的方式和浏览网站的方式来判断其是否是有价值的潜在客户，也就是在网购兴趣统计的基础上，模拟真实拍卖和销售场景中，销售员会对眼前的客户给出判断，来决定跟谁交流一样，由后台导购人员发起一条会话消息给用户，在用户的浏览页面上或浏览页面的右下脚进行提示，邀请用户进行一番对话，解答他们的问题，为他们提出建议。</p>
