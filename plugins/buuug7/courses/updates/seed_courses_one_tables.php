            <p style="text-indent: 2em">数字商品的特征是，数字产品的高固定成本，低增量成本，数字产品的易仿性，信息产品是“经验产品”，信息产品的用户的适用性和用户偏好的依赖性，可共享性和公共产品属性，增值传输过程和适用中的重新创造过程</p>
            <h4>电子商务参与者</h4>
            <p style="text-indent: 2em">政府是电子商务的倡导者和推动者，我国政府应考虑的问题：政府业务的转型、政府导向、CA问题。政府也是最主要的推动者和消费者。</p>
