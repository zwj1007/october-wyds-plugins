            <p style="text-indent: 2em">平台开设BBS论坛，发布平台公告，由后台管理人员开设板块，设置版主，供用户讨论各种有趣的话题，发布信息。在BBS上可以发表一个主题，让大家一起来探讨，也可以提出一个问题，大家一起来解决等。增强买卖双方的交流，发展人气。</p>
            
            <h3>业务交易平台</h3>
            <h4>订购报价</h4>
            <p style="text-indent: 2em">消费者可以将有购买意向的商品放入购物车，允许一次购买多件商品。可以对购物车内的商品进行修改，查看商品的详细信息。确认要进行交易，可以进入结算中心。在结算中心，消费者对送货地址、送货方式、时间要求等物流信息进行确认，并可以索要发票。</p>
            <p style="text-indent: 2em">系统进行结算并报价，读取消费者的积分，根据消费者积分值享受对应打折优惠幅度。消费者进行订单确认，进入支付结算环节。</p>
