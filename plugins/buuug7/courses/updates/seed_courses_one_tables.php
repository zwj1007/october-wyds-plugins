            <h3>系统安全体系</h3>
            <p style="text-indent: 2em">系统安全是整个系统可靠运行和进行安全防范的基石，系统安全设计需要在统一设计的原则下，在不同的安全层次，在预防、检测和恢复等各个阶段，确保系统持续稳定运行，防止信息的损坏、泄露或被非法修改，并保证系统安全。</p>
            <p style="text-indent: 2em">系统安全体系结构分为五层：网络安全、操作系统级安全、用户级安全、功能模块级安全、数据库级安全。</p>
            <h4>操作系统级安全</h4>
            <p style="text-indent: 2em">操作系统级安全保证是整个安全中的第一层保护。它保证只有合法的、授权的用户才能上网，才能登录到服务器。它是利用网络操作系统本身的安全机制来实现的。</p>
            <h4>用户级安全</h4>
            <p style="text-indent: 2em">用户级安全保证是第二层保护。它保证每个授权用户只能操作其权限所允许的功能模块或业务。用户在使用时，系统将从用户安全性列表中读取该用户的Rights标识符（RID）、用户级别（ULV）。系统根据RID及ULV决定用户所拥有的功能和对象的可操作性。用户安全性列表是根据用户的身份、所从事的业务来决定其拥有的权力属性；用户所拥有的权限属性不同，进入系统后用户所面对的功能树就不同，他所能操作的功能模块也就不同，从而实现根据用户权限自动裁剪功能树的目的。</p>
            <h4>模块级安全</h4>
            <p style="text-indent: 2em">模块级安全保证是第三层保护。它保证授权用户在进入某一功能模块后，只能做其用户级别所允许的操作。用户安全性列表中的用户级别（ULV）是用户拥有的某一具体权力属性的使用级别。使用级别由低到高，用户对该模块的操作权力也就由小变大，随着级别的变化，用户所面对的操作界面也会自动裁剪变化。譬如说，该用户对本职业务功能是高级使用者，但对相关业务功能则可能只是普通使用者，对这些模块的修改与删除功能就会对他屏蔽，相应控件会不可见。</p>
            <h4>数据库级安全</h4>
            <p style="text-indent: 2em">数据库级安全是系统安全体系设计中最核心的一道屏障。数据库级安全遭到破坏的情况主要有三种：第一，数据向未授权用户泄露，或被未授权用户改动；第二，一些合法用户在获得信息时得到权限以外的信息；第三，由于一些具有合法权限用户的误操作，破坏了数据库中的数据。本系统用户密码使用md5不可以逆加密，保证用户密码安全。通过数据库安全机制的管理，可以防止以上情况的发生，从而加强数据库的安全。</p>
            
            
