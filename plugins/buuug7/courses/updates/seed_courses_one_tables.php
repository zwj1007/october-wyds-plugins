            </blockquote>
            <p style="text-indent: 2em">关于电子商务的上也模式有很多种，各国政府、学者、企业界人士根据自己所处的地位和对电子商务参与的角度和程度的不同，给出了许多不同的定义。电子商务分为：ABC、B2B、B2C、B2B2C、C2C、B2M、M2C、B2A、C2A。九大种电子商务模式！</p>
            <h4>ABC模式解释</h4>
            <p style="text-indent: 2em">是由代理商（Agents)、商家（Business）和消费者（Consumer）共同搭建的集生产、经营、消费为一体的电子商务平台。相互之间可以转化。大家都是这个平台的主人，生产者，消费者，经营者，合作者，管理者，大家相互服务，相互支持，你中有我，我中有你，真正形成一个利益共同体，资源共享，产、消共生而达到共同幸福的良性局面，从而达到共产、共消、共福</p>
            <h4>B2C模式解释</h4>
            <p style="text-indent: 2em">B2C(Business to Customer)。B2C中的B是Business，意思是企业，2则是to的谐音，C是Customer，意思是消费者，所以B2C是企业对消费者的电子商务模式。这种形式的电子商务一般以网络零售业为主，主要借助于Internet开展在线销售活动.</p>
            <h4>B2B模式解释</h4>
            <p style="text-indent: 2em" >B2B（Business To Business，在英文中的2的发音同to一样。）是企业与企业之间通过互联网进行产品、服务及信息的交换。B2B是企业与企业之间通过互联网进行产品、服务及信息的交换。</p>
            <h4>B2B2C模式解释 </h4>
            <p style="text-indent: 2em"> 所谓B2B2C是一种新的网络通信销售方式，是英文“business to business to Consumer”的简 称。第一个B指广义的卖方（即成品、半成品、材料提供商等），第二个B指交易平台，即提供卖方与买方的联系平台，同时提供优质的附加服务，C即指买方。卖方不仅仅是公司，可以包括个人，即一种逻辑上的买卖关系中的卖方。平台绝非简单的中介，而是提供高附加值服务的渠道机构，拥有客户管理、信息反馈、数据库管理、决策支持等功能的服务平台。买方同样是逻辑上的关系，可以是内部也可以是外部的。B2B2C定义包括了现存的B2C和C2C 平台的商业模式，更加综合化，可以提供更优质的服务.</p>
            <h4>C2C模式解释</h4>
            <p style="text-indent: 2em">C2C的意思就是个人与个人之间的电子商务。比如一个消费者有一台旧电脑，通过网络进行交易，把它出售给另外一个消费者，此种交易类型就称为C2C电子商务。</p>
            <h4>B2M模式解释</h4>
            <p style="text-indent: 2em">B2M是相对于B2B、B2C、C2C的电子商务模式而言，是一种全新的电子商务模式。而这种电子商务相对于以上三种有着本质的不同，其根本的区别在于目标客户群的性质不同，前三者的目标客户群都是作为一种消费者的身份出现，而B2M所针对的客户群是该企业或者该产品的销售者或者为其工作者，而不是最终消费者。</p>
            <h4>M2C模式解释</h4>
            <p style="text-indent: 2em">M2C的概念，M是media的缩写，也就是媒介对消费者的购物模式。这是一种基于网上B2C,C2C基础之上的网上购物模式。 首先，在这个模式上有两层media身份的参与者，一个是像败物网这样的媒介平台本身，另一个就是在上面发布自己的或帮助别人发布商品信息进行推广的人，这些商人也是一种媒介或者说经理人，其代表的是商品。这种模式的本质在于促进网上购物商品信息不对称的解决。</p>
            <h4>B2A模式解释</h4>
            <p style="text-indent: 2em">B2A商业模式：B2A商业机构对行政机构(Business-to-administrations)的电子商务指的是企业与政府机构之间举行的电子商务活动。例如，政府将采购的细节在国际互联网络上公布，通过网上竞价方式举行招标，企业也要通过电子的方式举行投标。</p>
            <h4>C2A模式解释</h4>
            <p style="text-indent: 2em">消费者对行政机构的电子商务，指的是政府对个人的电子商务活动。这类的电子商务活动目前还没有真正形成。然而，在个别发达国家，如在澳大利亚，政府的税务机构已经通过指定私营税务，或财务会计事务所用电子方式来为个人报税。这类活动虽然还没有达到真正的报税电子化，但是，它已经具备了消费者对行政机构电子商务的雏形。</p>
            
