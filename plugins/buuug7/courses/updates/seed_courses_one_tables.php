           <p>近年来，国家出台了一系列政策鼓励、支持、引导县域经济的发展。我国《“十二五”规划纲要》也提出要“增强县域经济发展活力。扩大县域发展自主权，稳步推进扩权县改革试点。规划提出的发展战略，为县域经济的全方位发展，提供了重要的发展机遇。</p>
