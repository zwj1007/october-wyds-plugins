            <p>最近，阿里在医保支付领域的“战果”浮出水面。5月9日，蚂蚁金服旗下支付宝宣布面向个人用户推出一站式的“医疗服务”平台。“除了整合支付宝已有的挂号就诊等服务，用户通过该平台还可以获取健康咨询、健康资讯、母婴服务、健康金融等15项健康管理服务。”</p>
            <p>时代周报记者注意到，今年以来，蚂蚁金服越来越呈现基础设施化的趋势。医疗健康领域也成为蚂蚁金服打造开放的重要标本。</p>
