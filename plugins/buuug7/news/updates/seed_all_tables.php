            <p>我几年前开始就经常思考这个问题，当然也没想出个结果。或许会有不知天高地厚的年轻人说：如果你工作了十几年还没有掌握一些让自己不可替代的能力，被淘汰就是活该。这话听起来正确得像真理一样，我只希望说这话的人自己到了35岁的时候真的能够成为无法被年轻人代替的人。</p>
            <p>这世界上哪有那么多的不可代替？有肯定是有的，但绝大多数人所从事的工作真不需要十年的经验积累。如果你做的事情年轻人能做到80%甚至只是60%，而他要的薪水只有你的一半不到，你觉得老板会怎么选择？</p>
            <p>人到中年，身体机能开始退化了，你怎么跟那些二十来岁的毛头小伙子拼加班拼熬夜？就算你身体是铁打的，你要不要照顾家庭？要不要陪伴孩子？大家同在一个公司，人家加班你不加班，老板看了会怎么想？换了你是老板你会怎么想？你会仅仅因为同情或旧情而允许一个工作量比别人少拿钱还比别人多的人存在吗？</p>
            <p>其实代入NBA的情况就明白了，奥尼尔、邓肯、加内特、司机、科比等超巨能打到40岁左右自己不想打了才退休，而绝大多数普通球员不到35岁就打不上球了。三十二三岁以后即使还能拿到合同的一般都是低薪老将合同，像科比司机那样职业末年还能拿高薪的，那是人家之前为球队贡献了太多，这样的人在整个联盟能有几个？</p>
            <p>下面再看看我身边的例子：</p>
            <p class="lead">30多岁就当总经理，却感叹没事业</p>
            <p>我老婆刚毕业的时候，去了一家十几个人的润滑油销售公司做文员。</p>
            <p>那家公司是一家港资公司的子公司，总经理30多岁，一个月工资加销售提成大概有一万多块，在2006年的广州，在当时的我们看来是非常令人羡慕的高薪。</p>
            <p>他的生活确实也过得不错，有房有车还有闲钱弄点收藏，但我老婆（那时还是女朋友）说他有时会在公司闲聊时感叹没有事业。女友当时毕竟年轻，说X总您明明是事业有成啊怎么会没有事业？</p>
            <p>那总说：男人得自己创业，才算是有自己的事业。当时我们都不理解这话，后来人过三十以后，慢慢地发现，自己所在的公司里真的没有“老”员工了，自己的同学朋友也一个个不再打工了。至于当初女友公司的那个X总，在大约2009年的时候，公司解散，他得到了十几万的赔偿，后来也不知道做什么去了。</p>
            <h3>35岁后，白领们都去哪儿了？</h3>
            <p>后来我在一家互联网公司做小主管，下面有两个员工，其中一个近四十岁，是负责跑市场商务合作的，一看就是那种职场老油条，精明滑头，态度谦和谨慎，我对他也很客气。</p>
            <p>后来得知他在南山区（深圳房价最高的区）有两套房，不由肃然起敬。后来我们先后离开了那家公司，接下来两年里听说他又换了几次工作，终于不再打工，跟我说养老去了。他是早早享受到房价红利完成了资本积累的人，说养老是真有养老的条件。而现在那些连房都买不起的人，以后怎么养老？</p>
            <p>我同学，建筑设计专业的，自称中国每个大城市都有他设计的房子，三年前混到税前街薪的水平，辞职，和老婆开了家卖玉石的店，生意不错，说能跟得上深圳房价的涨幅。</p>
            <p>我同学，市政设计专业的，毕业后一路颇顺，是同学中第一个在一线买房的人。去年底辞职，自己在家乡那个五线小城市开了个小公司，目前尚未打开局面。</p>
