            <p>2016年11月8日，人社部发布《关于印发“互联网+人社”2020行动计划的通知》，明确社保卡将加载支付功能，支持各类缴费和待遇享受应用，同时将与商业银行、第三方支付平台等合作，建设统一、开放的医保结算数据交换接口，在安全可控的前提下，支持相关机构开展网上购药等应用。</p>
            <p>在此政策明朗之前，无论是阿里、腾讯，还是平安都在抢先布局这一生态圈，挺入医保移动支付领域。</p>
            <p>据时代周报记者观察，这场战役首先在去年上半年展开，地点是在深圳。去年以来，深圳市正式启动医疗保险移动支付试点工作，成为全国首个开展该试点的城市。</p>
            <p>2016年4月，腾讯与深圳市人社局签署战略合作协议。根据协议规定，深圳市的医保用户通过微信绑定医保卡，就可以在指定医院就诊时一键完成医保及自费金额的在线支付。</p>
            <p>作为使用概率最高的支付方式之一，支付宝亦不甘放过巨大的医疗支付蓝海，绑定医保支付一直是其想要攻坚的堡垒。阿里将医保支付的触角伸至腾讯和平安总部所在地深圳。</p>
            <p>2016年5月31日，支付宝同样与深圳市人社局共同合作医保移动支付项目，正式在深圳多家医院落地试运行，通过支付宝绑定金融社保卡后，可在手机上完成医保的门诊挂号和缴费。</p>
            <p>据深圳市人社局介绍，医疗保险的参保人只要通过合作方的支付平台（支付宝、微信、平安壹钱包）绑定加载金融功能的社会保障卡后，即可通过手机完成医保的门诊挂号和缴费，避免长时间排队。</p>
            <p>“市民可分别在支付宝、微信的‘城市服务’功能中，找到绑定社保卡、挂号及缴费的入口。平安壹钱包的用户则可以在合作医院的APP中，直接使用平安壹钱包绑卡及付费。”深圳市人社局相关负责人表示。</p>
            <p>据介绍，首批试点包括深圳市人民医院等17家医院，患者可通过手机完成医保的门诊挂号和缴费。一旦时机成熟，将向全市所有定点医院开放医保移动支付。</p>
            <p>拥有了前期的合作意向，腾讯和支付宝都想将此落地。事实上，巨头们选择深圳，其实是看中了这一城市年轻化的患者群体，以及庞大的就诊群体。</p>
            <p>深圳市卫计委此前提供给时代周报记者的数据显示，在“十二五”期间，深圳市财政卫生总投入599.1亿元，大胆改革，目前已初步建成基本适应近2000万人口需求的医疗卫生服务体系。</p>
            <p>最近，罗湖医院集团称，罗湖区人民医院门诊微信支付功能已全面开通，现在深圳市一档参保人可通过微信公众号“深圳罗湖区人民医院”进行医保缴费。</p>
            <p>5月15日，罗湖医院集团相关人士在接受时代周报记者采访时表示：“我们正在与支付宝商谈合作，待相关部门审批，通过之后上线。”</p>
