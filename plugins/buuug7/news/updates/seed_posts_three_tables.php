            <h4>媒体身份价值的电商业务实现</h4>
            <p style="text-indent: 2em">在不具备平台层次的电商业务接入的其它媒体中，多采取了利用媒体品牌背书功能以及基本的流量入口功能进行业务拓展，主要表现形式是从事微电商业务，将广告置换物资以及自有客户相关物资通过微电商平台，诸如微信公众号、微店、微商城等电子化平台进行销售，而此时的流量导入主要依赖自有的传统媒体以及微博、微信等的新媒体账号，此种业务模式一般都是一些媒体的一个或者两个业务部门的部分小团队在探索性运营，诸如南都报系的南都乐购、南都供销社，广州日报的微管家等平台。</p>
