            <p style="text-indent: 2em">四、媒体电商的价值意义</p>
            <p style="text-indent: 2em">媒体转型从一般意义上说有两个选择方向，其一是针对新生市场的进攻策略，进行增量空间的拓展，对应现实的举措就是新媒体渠道和平台的拓展和经营，实现新的业务增量，在这一点上，更多的传统媒体所作所为的效果都是十分有限的，或者说增量无法实现；</p>
            <p style="text-indent: 2em">其二是针对固有模式所形成的资源沉淀之后的价值挖掘，所谓的媒体资源变现的举措，诸如媒体品牌，媒体影响力，媒体资源的变现，而在这样的一个实践过程中，其变现的时间窗口十分之关键，就如一个女子选择结婚对象的整个过程中的变化一样，在年轻貌美之时不能高效完成婚姻对象的选择，意味着随着时间的推移其自身的价值在缩水，或许其变现的价值将大大折扣。</p>
            <p style="text-indent: 2em">媒体资源变现的路径选择，也同时意味着在从事媒体电商业务过程中，其核心定位未必在脱离媒体业务和资源储备本身，进行所谓全新的电商平台增量业务的打造，而是基于当下的资源优势，进行适当的路径选择和切入口的突破，因此媒体电商业务模式的选择与通常意义上的电商业务选择具有诸多的特殊性，更多是以媒体资源存量基础之上的价值变现过程中，将电商业务作为这种变现过程可以承载的载体，而非一个独立的业务体系，也不是一个完全脱离媒体属性的增量业务去对待，那样的电商业务注定无法成功。</p>
            <h3>五、当下全国媒体电商业务发展现状与展望</h3>
            <p style="text-indent: 2em">在电子商务发展繁荣的曾经，已经有众多的传统媒体选择将电商业务实践作为其转型尝试的一个方向，经过长时间的摸索实践，已经积累了丰富的实践经验，并且一些具有典型意义和示范作用的项目已经脱颖而出，总结过往全国同行的实践，大概可以有三种层次和方向。</p>
            <h4>1、平台级的媒体电商业务</h4>
            <p style="text-indent: 2em">这种模式是一种集体行动，从战略层面全线介入到电商产业链条之中，虽然在国内媒体中并不多见，但是依然有突出表现者，浙报集团旗下的《钱江晚报》发起的“窝里快购”电商平台，通过整合媒体原有的版面资源，发行渠道资源等，进行针对杭州本地市民生活消费产品的电商平台，降低了整个电商业务的成本率，其中物流成本就节约了90%，而且通过媒体背书，整合了诸如物业公司等民营机构进行紧密战略合作，并充分共享旗下的社区渠道资源平台，共同建设起来了一个区域垂直电商平台。</p>
            <p style="text-indent: 2em">与此同时，平台物流服务部分还承接来自淘宝等电商平台的物流配送服务，充分利用了媒体原有报纸配送队伍的资源优势，最大化现有资源的边际效用，是媒体电商中的典型代表。</p>
            <p style="text-indent: 2em">而青岛日报、成都商报等媒体，借助自己媒体身份优势以及电商业务的早期介入经验积累，承担了当地政府淘宝天猫等成熟电商平台的地方馆运营任务，一方面实现了对于一个区域电商产品和业务的深度介入，以及进行资源整合的优势，另一方面又通过服务政府赚得了一定的收入。</p>
            <p style="text-indent: 2em">更为重要的是通过整合当地资源进入阿里电商平台的过程，真正接触到成熟电商的整个商业流程，包括商品选择，技术平台使用，物流配送任务对接，商店流量运营，用户运营等的基本运作规律，为自有电商平台的搭建锻炼了队伍。</p>
            <p style="text-indent: 2em">在整个服务过程中，成都商报、青岛日报的电商业务负责人李天啸、孙成富等一批85后媒体人得到了阿里方面的高度认可，也成为传统媒体人中不可多得的谙习电商业务核心内核的综合人才。</p>
            <p style="text-indent: 2em">另外，一些地方的媒体，包括四川新疆当地的传统媒体，通过与政府部门中的农委和工信委等职能部门的合作，承接建设地方特色农产品商贸平台，实现了地方特色农产品的自有电商平台，面向全国的市场进行推广运营，一方面又以地方特色馆的身份进入京东、淘宝、天猫、1号店等的平台，另一方面又通过媒体传播平台，微电商的方式实现入口流量的导入，已经初步形成了特色农产品电商品牌和业务体系，商业闭环已经基本形成，这样的平台由政府承担主要的资金投入部分，而媒体则主要承接其基本的运营任务，是在政策红利背景下的电商业务的实际操练，现实价值十分明显。</p>
            <p style="text-indent: 2em">另外，拥有一些线下实体店资源的一些媒体，诸如徐州日报、杭州日报等，通过自有渠道和合作渠道，建立起遍及其城市和乡村的实体电商体验店，通过媒体平台进行流量导入，进行特色产品的线上传播销售，线下实体店体验成交的方式实现区域内的，特色商品的O2O电商业务，也是在媒体资源优势和线下渠道优势的基础上，充分挖掘资源变现的可能性和潜能，实现的电商业务模式的创新。</p>
            <p style="text-indent: 2em">特别是徐州日报，通过整合线下商场渠道资源的方式，改变过往广告置换现实商品的固有模式，通过置换日益不景气的现象商场柜台等资源，将自有的商品品类以及广告置换物资进行专柜销售，成功进入实体销售领域，同时利用媒体线上传播进行流量导入，一方面促进了自我商品的销售，又进一步带动了整个商场的人流量，取得了诸方收益多赢的局面，在商业模式设计方面取得了不错的成绩，这也是在前期简单商品销售模式基础上的模式创新所得，创新意味十足。</p>
